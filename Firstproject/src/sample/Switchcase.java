package sample;

public class Switchcase {
	public static void main(String[] args) {
		char vowel = 'a';
		
		switch(vowel) {
		case 'a':
			System.out.println("its a vowel");
		break;
		
		case 'e':
			System.out.println("its a vowel");
		break;
		
		case 'i':
			System.out.println("its a vowel");
		break;
		
		case 'o':
			System.out.println("its a vowel");
		break;
		
		case 'u':
			System.out.println("its a vowel");
		default:
		    System.out.println("its not a vowel");
		
		}
	}

}
